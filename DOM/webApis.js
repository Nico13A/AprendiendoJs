// ************************* WEB APIs *************************

/*
API significa Interfaz de Programación de Aplicaciones 
(por sus siglas en inglés, Application Programming Interface). 

Una Web API, específicamente, es un conjunto de reglas y herramientas que permite que 
diferentes aplicaciones de software se comuniquen entre sí a través de la web. 
Estas interfaces permiten que diferentes servicios web interactúen de 
manera eficiente y compartan datos entre ellos.
*/

// ----------------------------------------------------------

/* DOM: Document Object Model
El Modelo de Objetos del Documento es un API para documentos HTML y XML.

Éste proveé una representación estructural del documento, 
permitiendo modificar su contenido y presentación visual mediante código JavaScript.

El DOM no es parte de la especificación de JavaScript, es una API para los navegadores.
*/

// ----------------------------------------------------------

/* DOM, BOM Y CSSOM
El DOM se centra en la representación y manipulación de la estructura del documento HTML o XML, 
el BOM proporciona acceso a funciones específicas del navegador, 
y el CSSOM se ocupa de la representación y manipulación de las reglas de estilo CSS. 
Estos tres conceptos, combinados, son esenciales para el desarrollo web moderno e interactivo.

En otras palabras, el DOM es la manipulación del HTML mediante js y CSSOM es la manipulación 
del CSS mediante js.
El BOM no es una API estándar.
*/

